package com.lemon.Good.db;

public class GoodBean {
	private int go_num;
	private String go_user_email;
	private int go_category;
	private int go_text_num;

	public int getGo_num() {
		return go_num;
	}

	public void setGo_num(int go_num) {
		this.go_num = go_num;
	}

	public String getGo_user_email() {
		return go_user_email;
	}

	public void setGo_user_email(String go_user_email) {
		this.go_user_email = go_user_email;
	}

	public int getGo_category() {
		return go_category;
	}

	public void setGo_category(int go_category) {
		this.go_category = go_category;
	}

	public int getGo_text_num() {
		return go_text_num;
	}

	public void setGo_text_num(int go_text_num) {
		this.go_text_num = go_text_num;
	}

}
